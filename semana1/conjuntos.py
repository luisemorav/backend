# Sets | Inalterable
# Es una estructura desordenada y no indexado (indice)

estaciones = {"Verano", "OtoÃ±o", "Primavera", "Invierno"}
print(estaciones)

# add -> agregar un valor al conjunto
estaciones.add("Eder")
print(f'add -> {estaciones}')

# remove -> eliminar un valor al conjunto
estaciones.remove("Eder")
print(f'remove -> {estaciones}')